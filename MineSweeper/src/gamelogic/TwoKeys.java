package gamelogic;

/**
 *
 * @author libik
 */
public class TwoKeys {
    private int key, value;

    public TwoKeys(int key, int value){
        this.key = key;
        this.value = value;
    }

    /**
     * @return the key
     */
    public int getKey() {
        return key;
    }

    /**
     * @return the value
     */
    public int getValue() {
        return value;
    }

}
